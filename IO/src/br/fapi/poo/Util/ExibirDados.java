package br.fapi.poo.Util;

public class ExibirDados {

    public void exibirString(String variavel) {
        System.out.println(variavel);

    }

    public void exibirString(String mensagem, String variavel) {
        System.out.println(mensagem + variavel);

    }

    public void exibirInt(int variavel) {
        System.out.println(variavel);

    }

    public void exibirInt(String mensagem, int variavel) {
        System.out.println(mensagem + variavel);

    }

    public void exibirDouble(double variavel) {
        System.out.println(variavel);

    }

    public void exibirDouble(String mensagem, double variavel) {
        System.out.println(mensagem + variavel);

    }

}
